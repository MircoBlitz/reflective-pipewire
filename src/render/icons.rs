/// Built-in icon library. Returns SVG path data for a 144x144 viewBox.

pub fn get(name: &str) -> &'static str {
    match name {
        "mic" => MIC,
        "headset" => HEADSET,
        "headphones" => HEADPHONES,
        "stereo" => STEREO,
        "jack" => JACK,
        "dac" => DAC,
        "apps" => APPS,
        "discord" => DISCORD,
        "obs" => OBS,
        "twitch" => TWITCH,
        "speaker" => SPEAKER,
        "mixer" => MIXER,
        _ => MIC,
    }
}

